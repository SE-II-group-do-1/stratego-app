package com.example.stratego_app.model;

public class Position {
    int x;

    public Position(int x, int y) {
        this.x = x;
        this.y = y;
    }

    int y;

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
